package com.tananaev.wwdc.schedule.model;

import java.io.Serializable;

public class ImageVariant implements Serializable {

    private int width;

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    private int height;

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

}
